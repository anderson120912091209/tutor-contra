export function SocialLinksDisplay({ socialLinks }: SocialLinksDisplayProps) {
  const activeSocials = SOCIAL_PLATFORMS.filter(
    (platform) => socialLinks[platform.name as keyof SocialLinks]
  );

  if (activeSocials.length === 0) {
    return null;
  }

  return (
    <div className="flex items-center gap-3">
      {activeSocials.map((platform) => {
        const url = socialLinks[platform.name as keyof SocialLinks];
        return (
          <a
            key={platform.name}
            href={url}
            target="_blank"
            rel="noopener noreferrer"
            className={`
              p-2.5 rounded-full border-2 border-gray-200
              bg-white text-gray-700
              transition-all duration-200
              ${platform.color} hover:text-white hover:border-transparent
              hover:scale-110 hover:shadow-md
            `}
            aria-label={platform.label}
            title={platform.label}
          >
            {platform.icon}
          </a>
        );
      })}
    </div>
  );
}

